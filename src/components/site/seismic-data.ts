/**
 * Map по имени региона для быстрого join с GeoJSON.
 */
export const SEISMIC_MAP: Record<string, RegionSeismicData> = SEISMIC_DATA.reduce(
  (acc, r) => {
    acc[r.name] = r;
    return acc;
  },
  {} as Record<string, RegionSeismicData>
);

/**
 * Топ-5 самых сейсмоопасных регионов (для боковой панели «лидеров»).
 */
export const TOP_HAZARDOUS = [...SEISMIC_DATA]
  .filter((r) => r.level >= 4)
