/**
 * Данные по сейсмической опасности регионов России.
 *
 * Источник: карты ОСР-2018 (Общее сейсмическое районирование территории
 * Российской Федерации), ОСР-97, СНиП СП 14.13330.2018
 * «Строительство в сейсмических районах».
 *
 * Шкала: баллы MSK-64 / ШСМ-2017 (по 12-балльной шкале).
 *
 * Уровни опасности:
 *   1 (≤3 балла)   — Низкая        — earthquakes практически не фиксируются
 *   2 (4–5 баллов) — Умеренная     — слабые ощущаемые сотрясения
 *   3 (6–7 баллов) — Средняя       — возможны повреждения зданий
 *   4 (8 баллов)   — Высокая       — существенные повреждения, сейсмостойкое строительство обязательно
 *   5 (≥9 баллов)  — Очень высокая — разрушительные землетрясения
 *
 * card — дайджест для info-панели.
 */

export type SeismicLevel = 1 | 2 | 3 | 4 | 5;

export interface RegionSeismicData {
  /** точное имя из GeoJSON (используется для join) */
  name: string;
  /** уровень опасности 1..5 */
  level: SeismicLevel;
  /** максимальная расчётная интенсивность, баллы MSK-64 */
  intensity: number;
  /** краткое описание региона в контексте сейсмоопасности */
  note: string;
  /** ключевое историческое событие (если было) */
  event?: { year: string; mag?: number; title: string };
}

